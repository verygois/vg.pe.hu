    2017.3.22 <a href="http://vg.pe.hu/compilation/03/">Very Gois #3</a> を発表<br/>
    2017.10.10 <a href="http://vg.pe.hu/compilation/04/">Very Gois #4</a> を発表</h4>
  <p>2016年春以降、音楽会・展覧会などのイベント企画を開始　<a href="http://vg.pe.hu/show/">vg.pe.hu/show</a></p>
